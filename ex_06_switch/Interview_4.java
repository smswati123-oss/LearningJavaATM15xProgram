package ex_06_switch;

public class Interview_4 {
    public static void main(String[] args) {
        char code = 'c';
        switch (code){
            default:
                System.out.println("Invalid code");
            case 'A':
                System.out.println("65");
                break;
                case 'B':
                    System.out.println("66");
                    break;

        }
    }
}
