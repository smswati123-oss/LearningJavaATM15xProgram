package ex02_Basics;

public class Lab13_Logical_Operao {
    public static void main(String[] args) {
        boolean a = true;
        System.out.println(a);
        System.out.println(!a);

        boolean b = true;
        System.out.println(!!b);

        boolean c1 = false && true;
        System.out.println(c1);

    }
}
