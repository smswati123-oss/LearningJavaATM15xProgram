package java_coding_test_11th_jan;

public class Test_09 {
    //Write a Java program to print numbers from 1 to 10 using a do-while loop.
    public static void main(String[] args) {
        int number = 1;
        do{
            System.out.println("number " + number);
            number++;
        }while (number<=10);
    }
}
